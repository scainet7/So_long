#include "../libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*res;
	size_t	i;
	size_t	n;

	if (!s)
		return (NULL);
	i = 0;
	n = (unsigned int)ft_strlen(s);
	if (start > n)
		len = 0;
	if (len > n - start)
		len = n - start;
	res = (char *)malloc(sizeof(char) * (len + 1));
	if (!res)
		return (NULL);
	while (i < len)
	{
		res[i] = s[start + i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
