#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*a;
	char	*b;

	a = (char *) dst;
	b = (char *) src;
	if (a == 0 && b == 0)
		return (dst);
	while (n > 0)
	{
		*a++ = *b++;
		n--;
	}
	return (dst);
}
